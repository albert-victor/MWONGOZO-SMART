def _merge_programmes(*groups: list[Programme]) -> list[Programme]:
    # Later groups overwrite earlier ones when codes collide.
    merged: dict[str, Programme] = {}
    for group in groups:
        for programme in group:
            merged[programme.code] = programme
    return list(merged.values())


